	public function beforeSave($options = array()) {
		if(isset($this->data['Vendor']['group_id']) && $this->data['Vendor']['group_id'] > 0) {
			App::import('model','Ledger');
			$Group = new Ledger();
			$Group->create();
			$Group->save(
				array(
				  'Ledger' => array(
								'group_id' 	=> 8,
								'parent_id' => 0,
								'name' 		=> $this->data['Vendor']['GL#']
							  )
				)
			);
				
			$this->data['Vendor']['group_id'] = $Group->id;
		}
		return true;
	}

